#---method dengan nilai balik---
=begin
    
Method ini disebut juga dengan istilah FUNGSI • Method yang akan mengembalikan sebuah nilai yang sesuai dengan tipe data dari method itu sendiri. • Misal sebuah method bertipe integer, maka method tersebut akan mengembalikan sebuah nilai bertipe integer
penjelasan panjangnya:

Setiap metode di Ruby akan mengembalikan nilai secara default. Nilai yang dikembalikan ini akan menjadi nilai pernyataan terakhir. Sebagai contoh:
    
=end

def test
    i = 100
    j = 10
    k = 0
end
var = test
puts var

#Metode ini, ketika dipanggil, akan mengembalikan variabel terakhir yang dinyatakan.